export function getCaptionsByCategory(category: Caption['category']): Caption[] {
  return captions.filter((c) => c.category === category);
}

export function getCaptionsByLanguage(language: Caption['language']): Caption[] {
  return captions.filter((c) => c.language === language);
}

export function getRandomCaption(): Caption {
  return captions[Math.floor(Math.random() * captions.length)];
}

export function getRandomCaptionByCategory(category: Caption['category']): Caption {
  const filtered = getCaptionsByCategory(category);
  return filtered[Math.floor(Math.random() * filtered.length)];
}

export function replacePlaceholders(text: string, actualMinutes: number): string {
  return text.replace('{actual}', `${actualMinutes} min`);
}
